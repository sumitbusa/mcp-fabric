package com.demo.refdata.model;

public class InvestorRefData {
    private String investorId;
    private String investorName;
    private String investorType;
    private String country;
    private String lei;
    private String relationshipManager;

    public String getInvestorId() { return investorId; }
    public void setInvestorId(String investorId) { this.investorId = investorId; }
    public String getInvestorName() { return investorName; }
    public void setInvestorName(String investorName) { this.investorName = investorName; }
    public String getInvestorType() { return investorType; }
    public void setInvestorType(String investorType) { this.investorType = investorType; }
    public String getCountry() { return country; }
    public void setCountry(String country) { this.country = country; }
    public String getLei() { return lei; }
    public void setLei(String lei) { this.lei = lei; }
    public String getRelationshipManager() { return relationshipManager; }
    public void setRelationshipManager(String relationshipManager) { this.relationshipManager = relationshipManager; }
}
